CREATE TABLE entity_tools (ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
        name VARCHAR(255) NOT NULL,
        company VARCHAR(255) NOT NULL,
        price FLOAT(53) NOT NULL,
        available BOOLEAN NOT NULL,
        PRIMARY KEY (id, price)) PARTITION BY RANGE(price);

CREATE TABLE part_tools_price_0_500 PARTITION OF entity_tools FOR VALUES FROM (1) TO (500);
CREATE TABLE part_tools_price_501_1000 PARTITION OF entity_tools FOR VALUES FROM (501) TO (1000);
CREATE TABLE part_tools_price_1001_2000 PARTITION OF entity_tools FOR VALUES FROM (1001) TO (2000);
CREATE TABLE part_tools_price_2001_3000 PARTITION OF entity_tools FOR VALUES FROM (2001) TO (3000);
CREATE TABLE part_tools_price_3001_4000 PARTITION OF entity_tools FOR VALUES FROM (3001) TO (4000);
